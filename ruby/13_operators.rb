a = 5
b = 10

# Aritmetik operatörler
puts a + b # Toplama
puts a - b # Çıkarma
puts a * b # Çarpma
puts b / a # Bölme
puts b % a # Modülüs (kalan)

# Karşılaştırma operatörleri
puts a == b # Eşit mi?
puts a != b # Eşit değil mi?
puts a < b # Küçük mü?
puts a > b # Büyük mü?
puts a <= b # Küçük eşit mi?
puts a >= b # Büyük eşit mi?

# Mantıksal operatörler
c = true
d = false

puts c && d # AND (ve)
puts c || d # OR (veya)
puts !c # NOT (değil)
