# Integer veri türü
age = 28

# Float veri türü
height = 1.75

# Numeric veri türlerinin işlemleri
sum = age + height #=> 29.75
difference = age - height #=> 26.25
product = age * height #=> 49.0
quotient = age / height #=> 16.0

# Karşılaştırma işlemleri
greater_than = age > height #=> true
less_than = age < height #=> false
equal_to = age == height #=> false

# Type conversion
age_to_float = age.to_f #=> 28.0
height_to_integer = height.to_i #=> 1

# Float'ı tam sayıya çevirirken yuvarlama da yapabiliriz
rounded_height = height.round #=> 2

# Random sayı üretmek için
random_number = rand(1..10) #=> rastgele bir sayı, 1 ve 10 arasında

